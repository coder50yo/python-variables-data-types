# Variables and Data Types in Python

# Integer
integer_number = 10
print(f"Integer: {integer_number}")

# Float
float_number = 10.5
print(f"Float: {float_number}")

# String
string_text = "Hello, Python!"
print(f"String: {string_text}")

# Boolean
boolean_value = True
print(f"Boolean: {boolean_value}")

# List
list_example = [1, 2, 3, 4, 5]
print(f"List: {list_example}")

# Tuple
tuple_example = (1, 2, 3, 4, 5)
print(f"Tuple: {tuple_example}")

# Dictionary
dictionary_example = {"name": "Alice", "age": 30}
print(f"Dictionary: {dictionary_example}")

# Set
set_example = {1, 2, 3, 4, 5}
print(f"Set: {set_example}")

# NoneType
none_value = None
print(f"NoneType: {none_value}")

